# This is the solution to the Arithmetic Operators problem on HackerRank
int_a = int(input('Enter a positive integer:'))
int_b = int(input('Enter another positive integer:'))
sum_a_b = int_a + int_b
diff_a_b = int_a - int_b
prod_a_b = int_a * int_b
print('The sum of the two integers is: ', str(sum_a_b))
print('The difference between the two integers is: ', str(diff_a_b))
print('The product of the two integers is: ', str(prod_a_b))